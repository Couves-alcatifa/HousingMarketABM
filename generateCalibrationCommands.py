print_command(HOUSE_SEARCH_OBFUSCATION_FACTOR="0.6")
print_command(HOUSE_SEARCH_OBFUSCATION_FACTOR="0.8")
print_command(HOUSE_PRICE_REDUCTION_FACTOR="0.005")
print_command(INITIAL_WEALTH_PER_AGE=400)
print_command(INITIAL_WEALTH_PER_AGE=800)
print_command(INITIAL_WEALTH_PER_AGE=400, INITIAL_WEALTH_PER_PERCENTILE=200)
